<?php

// Define two numbers for demonstration
$num1 = 10;
$num2 = 5;

// Addition
$addition = $num1 + $num2;
echo "Addition: $num1 + $num2 = $addition <br>";

// Subtraction
$subtraction = $num1 - $num2;
echo "Subtraction: $num1 - $num2 = $subtraction <br>";

// Multiplication
$multiplication = $num1 * $num2;
echo "Multiplication: $num1 * $num2 = $multiplication <br>";

// Division
$division = $num1 / $num2;
echo "Division: $num1 / $num2 = $division <br>";

// Modulus
$modulus = $num1 % $num2;
echo "Modulus: $num1 % $num2 = $modulus <br>";

// Exponentiation
$exponentiation = $num1 ** $num2;
echo "Exponentiation: $num1 ^ $num2 = $exponentiation <br>";
?>
